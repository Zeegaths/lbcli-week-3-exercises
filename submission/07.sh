#!/bin/bash

# The PSBT to analyze
psbt="cHNidP8BAHsCAAAAAhuVpgVRdOxkuC7wW2rvw4800OVxl+QCgezYKHtCYN7GAQAAAAD/////HPTH9wFgyf4iQ2xw4DIDP8t9IjCePWDjhqgs8fXvSIcAAAAAAP////8BigIAAAAAAAAWABTHctb5VULhHvEejvx8emmDCtOKBQAAAAAAAAAA"

# Decode the PSBT using bitcoin-cli and extract the recipient address with jq
recipient=$(bitcoin-cli -regtest decodepsbt "$psbt" | jq -r '.tx.vout[0].scriptPubKey.addresses[0]')

# For regtest network, change the prefix from tb1q to bcrt1q
bcrt_address=${recipient/tb1q/bcrt1q}

echo "$bcrt_address"